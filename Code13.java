package ConditionalStmt;

public class Code13 {
    public static void main(String[] args) {

        // Using while loop
        System.out.println("Using while loop:");
        int i = 1;
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }

        System.out.println(); // for a new line

        // Using for loop
        System.out.println("Using for loop:");
        for (int j = 1; j <= 10; j++) {
            System.out.print(j + " ");
        }
    }
}
